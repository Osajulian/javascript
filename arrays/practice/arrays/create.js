// create a new array using "New"
let arrayUsingNew = new Array('Trains', 'Planes', 'Automobiles');
console.log('arrayUsingNew: ', arrayUsingNew); // [ 'Trains', 'Planes', 'Automobiles' ]

// Note: This also works w/o the 'new' keyword
let arrayNoNew = Array('Trains', 'Planes', 'Automobiles');
console.log('arrayNoNew: ', arrayNoNew); // [ 'Trains', 'Planes', 'Automobiles' ]

// create a new Array using length 
let arrayUsingLength = new Array(3);
console.log('arrayUsingLength: ', arrayUsingLength.length)

// create a new array using bracket notation
let arrayUsingBracket = ['Trains', 'Planes', 'Automobiles'];
console.log('arrayUsingBracket: ', arrayUsingBracket); // [ 'Trains', 'Planes', 'Automobiles' ]